function CallC {
	module=$1; shift
	myRhel=$(cat /etc/redhat-release | cut -d" " -f3)
	[[ $myRhel == 'release' ]] && myRhel=$(cat /etc/redhat-release | cut -d" " -f4)

	eval ${module}-rhel${myRhel:0:1} $*
	return $?
} #CallC
export -f CallC

