ParseArgs() {
	source <(CallC parseArgs $*); 
	client="${unknownArgs%% *}"; 
	[[ ${client:0:1} == "-" ]] && unset client || unknownArgs="${unknownArgs##* }"
	return 0;
}

export -f ParseArgs
